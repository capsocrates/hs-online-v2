	<h4>Why won't my Google Doc open up in HS Online?</h4>
	<p>You first need to sign in to Google Docs in order to open up the doc.   Please do this <strong>before</strong> logging on to HeroSkype.  Maybe in the future, we will figure out a way to tie in the login with the Google Doc API...</p>

	<h4>What if I don't want to open up a Google Doc?</h4>
	<p>No sweat!  You don't have to open one up, and you can even resize your browser so that only the left column of HeroSkype tools is viewable.  That way, you can set it next to another browser window.
	</p>
