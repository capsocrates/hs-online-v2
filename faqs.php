	<h4>How does the Time Tracking Mechanism work?</h4>
	<p>For your convenience, the time used in-game is tracked for each player.  When a user sets Order Markers, the game clock is started; the next person to set Order Markers incurs the time between the initial OM placement.  The same thing happens when rolling for initiative.  This is to dissuade players from wasting time between rounds.</p>
	<p>Additionally, users can incur time after completing particular actions.  As such, users should press the 'Done' button when finishing an action, such as Scattering.  In the case of Scatter: when the attacking player initially attacks a scattering unit, his dice roll assigns the time that passed between the OM reveal and the attack roll to the attacker.  The player who is actually scattering should then press the 'Done' button after scattering any figures; this will assign to him any time that passed during their scatter, and reset the game clock.
	<p>For other actions, such as Engagement Strike, the corresponding quick menu button should be used in order to allocate the time that it takes a user to roll for the action (in this example: Engagement Strike).</p>
	<p>If a game needs to be paused, players can simply leave the game. Once all active players have returned, and have pressed the 'Resume a paused game' button, the game clock will be reset accordingly.</p>
