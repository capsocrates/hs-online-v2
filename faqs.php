	<h4>When I logon to my game, a different map/Google Doc loads in my browser.</h4>
	<p>You need to close all open browser sessions. Then, go to <a href="http://skorcard.net/tools/hs/online">HS Online main page</a> and  click on Logout.  This will clear your existing session, and you can then login to your game.
	</p>
	<h4>How can I roll blanks?</h4>
	<p>Under <strong>DICE TOOLS</strong>, place a check next to <strong>Roll Valkyrie dice</strong>.  This will display the symbols rolled, which are the same as blanks.
	</p>

